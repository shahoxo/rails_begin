class OrderNotifier < ActionMailer::Base
  default from: 'Super Device Center<super-device-center@device.force.eternal.com>'

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_notifier.received.subject
  #
  def received(order)
    @order = order

    mail to: order.email, subject: "Super Device Center Confirmation"
  end

  # Subject can be set in your I18n file at config/locales/en.yml
  # with the following lookup:
  #
  #   en.order_notifier.shipped.subject
  #
  def shipped(order)
    @order = order
    
    mail to: order.email, subject: "Super Device Center Confirmation"
  end
end
